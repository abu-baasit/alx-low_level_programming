/***
* add - function that adds two numbers
* @a: first number
* @b: second number
* Return:returns the addition of the two numbers
**/
int add(int a,int b)
{
	return (a + b);
}

/**
* sub - function that subtract two numbers
* @a: first number
* @b: second number
* Return:returns the subtraction of the two numbers
**/
int sub(int a,int b)
{
	return (a - b);
}

/**
* mul - function that multiply two numbers
* @a: first number
* @b: second number
* Return:returns the multiplication of the two numbers
**/
int mul(int a,int b)
{
	return (a * b);
}


/**
* div - function that divide two numbers
* @a: first number
* @b: second number
* Return:returns the division of the two numbers
**/
int div(int a,int b)
{
	return (a / b);
}

/**
* mod - function that gives remainder of two numbers
* @a: first number
* @b: second number
* Return:returns the remainder of the two numbers
**/
int mod(int a,int b)
{
	return (a % b);
}
